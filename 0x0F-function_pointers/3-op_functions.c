#include "3-calc.h"

/**
 * op_add - function that add a and b
 * @a: integer a
 * @b: integer b
 * Return: a+b
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that sub a and b
 * @a: integer a
 * @b: integer b
 * Return: a-b
*/


int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that multiplicate a and b
 * @a: integer a
 * @b: integer b
 * Return: a*b
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that dived a by b
 * @a: integer a
 * @b: integer b
 * Return: a/b
*/


int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that return a modulo b
 * @a: integer a
 * @b: integer b
 * Return: a%b
*/

int op_mod(int a, int b)
{
	return (a % b);
}
